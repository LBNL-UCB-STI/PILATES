from typing import Tuple
from abc import ABC
import logging
import os

from pilates.generic.runner import GenericRunner
from pilates.generic.records import RecordStore, ModelRunInfo
from pilates.workspace import Workspace
from workflow_state import WorkflowState
from pilates.utils.provenance import FileProvenanceTracker

logger = logging.getLogger(__name__)


class UrbansimRunner(GenericRunner):
    """
    Runner for the UrbanSim land use model.
    This class is responsible for running the UrbanSim containerized model.
    All preprocessing and postprocessing should be handled outside this method,
    following the BEAM/ActivitySim/ATLAS pattern.
    """

    def __init__(self, model_name: str):
        super().__init__(model_name)
        self.required_input_files = ["usim_data"]

    def run(
        self,
        store: RecordStore,
        state: WorkflowState,
        workspace: Workspace,
        provenance_tracker: FileProvenanceTracker,
    ) -> Tuple[RecordStore, ModelRunInfo]:
        """
        Executes the UrbanSim model run.

        Args:
            store (RecordStore): The input data generated by the preprocessor.
            state (WorkflowState): The workflow state.
            workspace (Workspace): The workspace object for path management.
            provenance_tracker (FileProvenanceTracker): The provenance tracker.

        Returns:
            tuple: A tuple containing:
                - data (RecordStore): The raw output files that have been prepared to run the model
                - model_run_info (ModelRunInfo): Information about the model run

        Notes:
            - All preprocessing and postprocessing should be handled outside this method.
            - This method only runs the UrbanSim container and records provenance for the run.
        """
        logger.info(
            "[UrbansimRunner] Starting UrbanSim model run for year %s",
            state.current_year,
        )
        settings = state.full_settings
        forecast_year = state.forecast_year

        # Prepare output file path
        usim_output_store_name = settings["usim_formattable_output_file_name"].format(
            year=forecast_year
        )
        usim_datastore_fpath = os.path.join(
            workspace.get_usim_mutable_data_dir(), usim_output_store_name
        )

        # TODO: Add logic to actually run the UrbanSim container here, similar to AtlasRunner/BeamRunner.
        # For now, just record the output file if it exists.

        output_records = []
        if os.path.exists(usim_datastore_fpath):
            output_rec = provenance_tracker.record_output_file(
                self.model_name,
                usim_datastore_fpath,
                year=forecast_year,
                description="UrbanSim forecast output data",
                model_run_id=None,  # To be filled in later
            )
            if output_rec:
                output_records.append(output_rec)
            logger.info(
                f"[UrbansimRunner] Recorded UrbanSim output file: {usim_datastore_fpath}"
            )
        else:
            logger.error(
                f"[UrbansimRunner] UrbanSim output file not found at {usim_datastore_fpath}"
            )

        # Return empty RecordStore and None for ModelRunInfo for now
        # In the future, this should return the actual outputs and run info
        return RecordStore(), None
